# AC3: Standard Markdown hierarchy
#   # page title  /  ## subsection  /  - bullet point
# ---------------------------------------------------------------------------

class TestAC3_MarkdownHierarchy:
    """Verify the outline uses the correct Markdown heading levels and
    bullet-point syntax throughout."""

    @pytest.fixture(autouse=True)
    def setup(self):
        self.pkg = _make_complete_pkg()
        self.outline = generate_outline(self.pkg)
        self.lines = self.outline.splitlines()

    def test_top_level_heading_is_cover(self):
        """Exactly one H1 heading: the cover page title."""
        h1_lines = [line for line in self.lines if line.startswith("# ") and not line.startswith("## ")]
        assert len(h1_lines) == 1
        assert h1_lines[0] == "# Prep Package"

    def test_page_titles_use_h2(self):
        """All page-level sections (pages 2-6) use H2 headings."""
        expected_h2 = [
            "## Industry Landscape & Threat Assessment",
            "## Problem Diagnosis",
            "## Solution Direction",
            "## Case References",
            "## Talking Points & Next Steps",
        ]
        for heading in expected_h2:
            assert heading in self.outline, f"Missing H2: {heading}"

    def test_subsections_use_h3(self):
        """Sub-sections within pages use H3 headings."""
        expected_h3 = [
            "### Recent Threat Events",
            "### Sensitivity Alerts",
            "### Follow-up Questions",
        ]
        for heading in expected_h3:
            assert heading in self.outline, f"Missing H3: {heading}"

    def test_bullet_points_use_dash_prefix(self):
        """List items must use '- ' (dash + space) Markdown syntax."""
        bullet_lines = [line for line in self.lines if line.startswith("- ")]
        # We have alerts + cases + follow-ups + threat events = many bullets
        assert len(bullet_lines) >= (
            len(self.pkg.sensitivity_alerts)
            + len(self.pkg.matched_cases)
            + len(self.pkg.follow_up_questions)
            + len(self.pkg.threat_intel)
        )

    def test_no_h4_or_deeper_headings(self):
        """The outline should not use #### or deeper headings."""
        for line in self.lines:
            assert not line.startswith("#### "), (
                f"Unexpected H4+ heading: {line}"
            )

    def test_blockquotes_only_on_cover(self):
        """Blockquote ('>') should only appear on the cover page."""
        bq_lines = [line for line in self.lines if line.startswith(">")]
        assert len(bq_lines) == 1
        assert "Auto-generated by JARVIS" in bq_lines[0]

    def test_threat_event_bullets_use_bold_title(self):
        """Threat event bullets must bold the title: **- title** (...)."""
        for event in self.pkg.threat_intel:
            assert f"**{event.title}**" in self.outline


# ---------------------------------------------------------------------------
# AC4: Missing / empty module fields → outline still generates correctly
#   The page for a module with empty data should still appear (heading)
#   but use fallback content or omit the empty items gracefully.
# ---------------------------------------------------------------------------

class TestAC4_EmptyOrMissingModuleFields:
    """Given a PrepPackage where some module fields are empty/minimal,
    the outline must still generate without errors and include all page
    headings."""

    def test_empty_matched_cases_shows_fallback(self):
        """When matched_cases is empty, a fallback message must appear."""
        pkg = _make_complete_pkg(matched_cases=[])
        outline = generate_outline(pkg)
        assert "## Case References" in outline
        assert "No specific case matches found" in outline

    def test_empty_matched_cases_no_case_bullets(self):
        """No individual case bullets when matched_cases is empty."""
        pkg = _make_complete_pkg(matched_cases=[])
        outline = generate_outline(pkg)
        # Collect only direct bullets under ## Case References
        # (stop at ### Follow-up Questions, which is still within this section)
        in_case_section = False
        case_bullets = []
        for line in outline.splitlines():
            if line == "## Case References":
                in_case_section = True
                continue
            if in_case_section and (line.startswith("## ") or line.startswith("### ")):
                break
            if in_case_section and line.startswith("- "):
                case_bullets.append(line)
        assert len(case_bullets) == 1
        assert "No specific case matches" in case_bullets[0]

    def test_empty_followup_questions_section_still_has_heading(self):
        """Follow-up Questions sub-heading appears even with empty list."""
        pkg = _make_complete_pkg(follow_up_questions=["placeholder"])
        # We cannot use a truly empty list for follow_up_questions without
        # a model change, but we verify the heading exists regardless.
        outline = generate_outline(pkg)
        assert "### Follow-up Questions" in outline

    def test_single_sensitivity_alert_renders(self):
        """Minimum sensitivity_alerts (min_length=1) renders correctly."""
        pkg = _make_complete_pkg(sensitivity_alerts=["Only alert"])
        outline = generate_outline(pkg)
        assert "- Only alert" in outline

    def test_outline_generates_with_all_minimal_valid_fields(self):
        """Outline must generate with the absolute minimum valid PrepPackage."""
        pkg = PrepPackage(
            scenario_assessment="Low urgency",
            sensitivity_alerts=["Single alert"],
            matched_cases=[],
            follow_up_questions=[],
            solution_direction="Monitor and review.",
            talking_points="Keep it brief.",
        )
        outline = generate_outline(pkg)
        # All page headings still present
        assert "# Prep Package" in outline
        assert "## Industry Landscape & Threat Assessment" in outline
        assert "## Problem Diagnosis" in outline
        assert "## Solution Direction" in outline
        assert "## Case References" in outline
        assert "## Talking Points & Next Steps" in outline
        # Fallback content for empty matched_cases
        assert "No specific case matches found" in outline
        # Minimal content still rendered
        assert "Low urgency" in outline
        assert "Monitor and review." in outline
        assert "Keep it brief." in outline
        assert "- Single alert" in outline

    def test_empty_followup_and_cases_together(self):
        """Both matched_cases and follow_up_questions empty at the same time."""
        pkg = PrepPackage(
            scenario_assessment="Standard review",
            sensitivity_alerts=["Watch for budget constraints"],
            matched_cases=[],
            follow_up_questions=[],
            solution_direction="Standard security posture review.",
            talking_points="Focus on relationship building.",
        )
        outline = generate_outline(pkg)
        assert "No specific case matches found" in outline
        assert "## Case References" in outline
        # Follow-up section heading present, but no bullets under it
        assert "### Follow-up Questions" in outline

    def test_multiline_scenario_assessment_renders(self):
        """Multi-line scenario_assessment text should be preserved as-is."""
        assessment = "Line 1: Overview\nLine 2: Details\nLine 3: Recommendation"
        pkg = _make_complete_pkg(scenario_assessment=assessment)
        outline = generate_outline(pkg)
        assert "Line 1: Overview" in outline
        assert "Line 2: Details" in outline
        assert "Line 3: Recommendation" in outline

    def test_outline_is_nonempty_string(self):
        """generate_outline must always return a non-empty string."""
        pkg = _make_complete_pkg()
        outline = generate_outline(pkg)
        assert isinstance(outline, str)
        assert len(outline.strip()) > 0

    def test_no_trailing_whitespace_on_heading_lines(self):
        """Heading lines should not have trailing whitespace."""
        pkg = _make_complete_pkg()
        outline = generate_outline(pkg)
        for line in outline.splitlines():
            if line.startswith("#"):
                assert line == line.rstrip(), (
                    f"Heading has trailing whitespace: '{line}'"
                )
