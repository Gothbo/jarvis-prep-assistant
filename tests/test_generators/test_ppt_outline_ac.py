"""Comprehensive acceptance tests for US-012: PPT Outline Generation.

Tests are organized by acceptance criteria (AC1-AC4) and verify the
generate_outline() function against the specification in the PrepPackage
model and the PPT outline generator.

Existing basic smoke tests live in test_ppt_outline.py; these tests
build on and extend that coverage without duplicating it.
"""

import pytest

from jarvis.generators.ppt_outline import generate_outline
from jarvis.models.prep_package import PrepPackage, ThreatEvent

# ---------------------------------------------------------------------------
# Helpers / Fixtures
# ---------------------------------------------------------------------------

def _make_threat_event(**overrides) -> ThreatEvent:
    """Create a ThreatEvent with sensible defaults, allowing field overrides."""
    defaults = dict(
        title="LockBit Ransomware Attack",
        date="2026-05-20",
        industry="Manufacturing",
        description="Major manufacturer hit by LockBit ransomware, production halted for 5 days.",
        source_url="https://example.com/threat/lockbit-2026",
    )
    defaults.update(overrides)
    return ThreatEvent(**defaults)


