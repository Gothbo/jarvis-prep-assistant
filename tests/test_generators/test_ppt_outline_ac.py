class TestAC2_PopulatedThreatIntel:
    """Given a PrepPackage with threat_intel, events must be rendered."""

    @pytest.fixture(autouse=True)
    def setup(self):
        self.events = [
            _make_threat_event(),
            _make_threat_event(
                title="Zero-Day in PLC Firmware",
                date="2026-06-10",
                industry="Energy",
                description="Critical zero-day vulnerability discovered in Siemens PLC firmware.",
                source_url=None,
            ),
        ]
        self.pkg = _make_complete_pkg(threat_intel=self.events)
        self.outline = generate_outline(self.pkg)

    def test_recent_threat_events_heading_present(self):
        assert "### Recent Threat Events" in self.outline

    def test_each_event_rendered_as_bullet(self):
        """Each threat event should appear as a Markdown bold-title bullet."""
        for event in self.events:
            expected = f"- **{event.title}** ({event.date}): {event.description}"
            assert expected in self.outline, (
                f"Missing event bullet for: {event.title}"
            )

    def test_event_order_matches_input(self):
        """Events should appear in the same order as the input list."""
        pos_first = self.outline.index("LockBit Ransomware Attack")
        pos_second = self.outline.index("Zero-Day in PLC Firmware")
        assert pos_first < pos_second

    def test_event_with_none_source_url_still_renders(self):
        """source_url=None should not prevent the event from appearing."""
        assert "Zero-Day in PLC Firmware" in self.outline


